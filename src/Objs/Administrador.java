public class Administrador {
    private int adm_cod;
    private String adm_nome;
    private String adm_telefone;
    private String adm_email;
    private String adm_usuario;
    private String adm_senha;
    
    public int getAdm_cod() {
        return adm_cod;
    }

    public void setAdm_cod(int adm_cod) {
        this.adm_cod = adm_cod;
    }

    public String getAdm_nome() {
        return adm_nome;
    }

    public void setAdm_nome(String adm_nome) {
        this.adm_nome = adm_nome;
    }

    public String getAdm_telefone() {
        return adm_telefone;
    }

    public void setAdm_telefone(String adm_telefone) {
        this.adm_telefone = adm_telefone;
    }

    public String getAdm_email() {
        return adm_email;
    }

    public void setAdm_email(String adm_email) {
        this.adm_email = adm_email;
    }

    public String getAdm_usuario() {
        return adm_usuario;
    }

    public void setAdm_usuario(String adm_usuario) {
        this.adm_usuario = adm_usuario;
    }
    
    public String getAdm_senha() {
        return adm_senha;
    }

    public void setAdm_senha(String adm_senha) {
        this.adm_senha = adm_senha;
    }
    
    
    
}
